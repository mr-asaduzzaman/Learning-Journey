function tenTimes(number){
    const Result = number * 10;
    return(Result);  // Return রিটার্ন এর পর কোনো কমান্ড কাজ করবেনা।
}

const output = tenTimes(4)
console.log(output)



function doMath(x,y){
const sum = x + y;
const sub = x-y;
const multiply = sum * sub;
const division = multiply / sum;
return division;
}

const Result = doMath(9, 5)
console.log(Result)



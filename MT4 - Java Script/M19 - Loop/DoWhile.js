// While Loop এবং Do while এর মধ্যে পার্থক্য হলো 
// While loop আগে শর্ত চেক করে পরে  আউটপুট দেখায় তারপর 
// Do whilee আগে আউটপুট দেখায় পড়্রে  চেক করে


let n = 4;
do {
    console.log(n);
    n++;
}
while (n < 5);

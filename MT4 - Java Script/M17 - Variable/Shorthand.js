// Addition Shorthand

var price = 200;
NewPrice = price + 100;
NewPrice += 100; // This is the short form


console.log(NewPrice);



// Subtraction Shorthand
var Rate = 5000;
DiscoundRate = Rate - 300;
DiscoundRate -= 300; // This is the short form

console.log(DiscoundRate)



//Multiply Shorthand
var Balance = 500;
Balance = Balance * 3;
Balance *= 3; // This is the short form


console.log(Balance);
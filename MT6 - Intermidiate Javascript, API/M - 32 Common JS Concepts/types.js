// JavaScript is a Dynamic Typed Programming Language 

// Primitive Type
const age = 25; 
const name = 'Rakib';
const isBangladeshi = true;

console.log(typeof age) // number
console.log(typeof name) // string
console.log(typeof isBangladeshi) // boolean




// Non-Primitive Types
const heights = [65,84,75,63,54,65];
const student ={name: 'Sakib', roll: 25}

console.log(typeof heights) // object
console.log(typeof student) //object